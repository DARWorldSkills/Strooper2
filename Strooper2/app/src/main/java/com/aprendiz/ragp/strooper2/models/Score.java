package com.aprendiz.ragp.strooper2.models;

public class Score {
    private String arcietos;
    private String porcentaje;

    public Score() {
    }

    public String getArcietos() {
        return arcietos;
    }

    public void setArcietos(String arcietos) {
        this.arcietos = arcietos;
    }

    public String getPorcentaje() {
        return porcentaje;
    }

    public void setPorcentaje(String porcentaje) {
        this.porcentaje = porcentaje;
    }
}
